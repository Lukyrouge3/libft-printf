#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t size)
{
	char			*d;
	const char		*s;
	unsigned int	dlen;
	unsigned int	n;

	d = dest;
	s = src;
	n = size;
	while (n-- != 0 && *d)
		d++;
	dlen = d - dest;
	n = size - dlen;
	if (n == 0)
		return (dlen + ft_strlen(s));
	while (*s)
	{
		if (n != 1)
		{
			*d++ = *s;
			n--;
		}
		s++;
	}
	*d = '\0';
	return (dlen + (s - src));
}
